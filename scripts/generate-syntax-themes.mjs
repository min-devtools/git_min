const cssRoles = [
  ["key", "keyword"],
  ["operator", "operator"],
  ["punctuation", "punctuation"],
  ["string", "string"],
  ["number", "number"],
  ["boolean", "boolean"],
  ["null", "constant"],
  ["variable", "variable"],
  ["function", "function"],
  ["parameter", "parameter"],
  ["type", "type"],
  ["property", "property"],
  ["comment", "comment"],
  ["constant", "constant"],
  ["tag", "tag"],
];

function readSyntax(source) {
  const values = {};
  let inSyntax = false;
  for (const rawLine of readFileSync(resolve(sourceDir, `${source}.toml`), "utf8").split(/\r?\n/)) {
    const line = rawLine.trim();
    if (line.startsWith("[")) {
      inSyntax = line === "[syntax]";
      continue;
    }
    if (!inSyntax) continue;
    const match = line.match(/^([a-z_]+)\s*=\s*"([^"]+)"/);
    if (match) values[match[1]] = match[2];
  }
  for (const [, sourceRole] of cssRoles) {
    if (!values[sourceRole]) throw new Error(`${source}.toml is missing syntax.${sourceRole}`);
  }
  return values;
}

const blocks = themes.map(({ ids, source }, index) => {
  const syntax = readSyntax(source);
  const selectors = ids.map((id) => `body[data-theme="${id}"]`);
  if (index === 0) selectors.unshift(":root", "body");
  const declarations = cssRoles.map(([cssRole, sourceRole]) => `  --syntax-${cssRole}: ${syntax[sourceRole]};`);
  return `/* ${source}.toml */\n${selectors.join(",\n")} {\n${declarations.join("\n")}\n}`;
});

const output = `/* Generated by scripts/generate-syntax-themes.mjs.
   Source of truth: ../netherize_editor/config/themes/*.toml [syntax]. */\n\n${blocks.join("\n\n")}\n`;
writeFileSync(outputFile, output);
console.log(`generated ${themes.length} semantic syntax palettes`);
